public class Ex6 {
    public static void main(String[] args) {
        float celsius = 10f;
        float fahrenheit = (9* celsius + 160)/5;
        System.out.println(celsius + " fica " + fahrenheit + " em fahrenheit");

        celsius = 25.3f;
        fahrenheit = (9* celsius + 160)/5;
        System.out.println(celsius + " fica " + fahrenheit + " em fahrenheit");

        celsius = -8f;
        fahrenheit = (9* celsius + 160)/5;
        System.out.println(celsius + " fica " + fahrenheit + " em fahrenheit");

        celsius = 0f;
        fahrenheit = (9* celsius + 160)/5;
        System.out.println(celsius + " fica " + fahrenheit + " em fahrenheit");
    }
}
